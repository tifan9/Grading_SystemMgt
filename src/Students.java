    private void table(){
        try{
            int i;
            pst= con.prepareStatement("select * from students");
            
            rs= pst.executeQuery();
            res= rs.getMetaData();
            i = res.getColumnCount();
            
            DefaultTableModel Df= (DefaultTableModel)stTable.getModel();
            Df.setRowCount(0);
            
            while(rs.next()){
                Vector v= new Vector();
                for(int j=1; j<=i; j++){
                    v.add(rs.getInt("id"));
                    v.add(rs.getInt("stid"));
                    v.add(rs.getString("fname"));
                    v.add(rs.getString("Lname"));
                    v.add(rs.getString("gender"));
                    v.add(rs.getString("phone"));
                    v.add(rs.getString("address"));
                    v.add(rs.getString("grade"));
                    
                }
                Df.addRow(v);
            }
        }catch(Exception e){
            e.printStackTrace();
        }
        
    }
