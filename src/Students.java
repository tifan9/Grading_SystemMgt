    private void savebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_savebtnActionPerformed
        // TODO add your handling code here:
        try {
            if(stidtxt.getText().trim().isEmpty() ||
                    fnametxt.getText().trim().isEmpty() || 
                    lnametxt.getText().trim().isEmpty() || 
                    addresstxt.getText().trim().isEmpty() || 
                    phonetxt.getText().trim().isEmpty() || 
                    gradetxt.getText().trim().isEmpty()){
                
                JOptionPane.showMessageDialog(null, "All Fields are Required","Empty fields",JOptionPane.WARNING_MESSAGE);
            }else if(stidtxt.getText().length()!= 5){
                JOptionPane.showMessageDialog(null, "Student ID Should be 5 characters","Error",JOptionPane.ERROR_MESSAGE);
                stidtxt.setText("");
            }else if(phonetxt.getText().length()!=13){
                JOptionPane.showMessageDialog(null, "Phone Number has to be +(00)000-0000-00");
                phonetxt.setText("");
            }
            else{
                int id= Integer.parseInt(stidtxt.getText());
                pst = con.prepareStatement("SELECT COUNT(*) FROM students WHERE stid = ?");
                pst.setInt(1, id);
                ResultSet rs = pst.executeQuery();
                rs.next();
                int count = rs.getInt(1);
                if(count > 0){
                    JOptionPane.showMessageDialog(null, "Student ID already exists in the database", "Error", JOptionPane.ERROR_MESSAGE);
                    stidtxt.setText("");
                }else{
                    pst  = con.prepareStatement("insert into students (stid, fname,lname,gender,phone,address,grade) values (?,?,?,?,?,?,?)");
                pst.setInt(1,id);
                pst.setString(2,fnametxt.getText());
                pst.setString(3,lnametxt.getText());
                
                if(malebtn.isSelected()){
                    gender = "Male";
                }
                if(femalebtn.isSelected()){
                    gender= "Female";
                }
                pst.setString(4, gender);
                pst.setString(5,phonetxt.getText());
                pst.setString(6,addresstxt.getText());
                pst.setString(7,gradetxt.getText());
                // executeUpdate
            int rowAffected= pst.executeUpdate();
            
            if(rowAffected<=1){
                JOptionPane.showMessageDialog(null, "Saved!");
                table();
                stidtxt.setText("");
                fnametxt.setText("");
                lnametxt.setText("");
                buttonGroup1.clearSelection();
                phonetxt.setText("");
                addresstxt.setText("");
                gradetxt.setText("");
                stidtxt.requestFocus();
                
            }
                }
                //int id= Integer.parseInt(stidtxt.getText());
                   
            }
            //con.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_savebtnActionPerformed
