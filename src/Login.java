    private void loginbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginbtnActionPerformed
        // TODO add your handling code here:
        // login for the admin only
            String emailad = "admin@gmail.com";
            String passwordad= "admin";
            if(emailad.equals(emailtxt.getText()) && passwordad.equals(passtxt.getText())){
                      Registration register= new Registration();
                      register.setVisible(true);
                      dispose();  
                    }
        try {
            
            // Check for validation
            if(emailtxt.getText().trim().isEmpty()){
                JOptionPane.showMessageDialog(this, "email required");
            }
            else if(passtxt.getText().trim().isEmpty()){
                JOptionPane.showMessageDialog(this, "Password required");
            }else{
                String email= emailtxt.getText();
                String password= passtxt.getText();
                //connect to the database
                pst=con.prepareStatement("select * from users where email = ? and password= ?");
                pst.setString(1, email);
                pst.setString(2,password);
                
                // execute statement
                rs=pst.executeQuery();
                if(rs.next()){
                    JOptionPane.showMessageDialog(null, "Login Successfully");
                    Students student= new Students();
                      student.setVisible(true);
                      dispose();
                }else{
                    JOptionPane.showMessageDialog(null,"Email and Password does not match","Error",JOptionPane.ERROR_MESSAGE);
                }
                
                
            
            }
            
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_loginbtnActionPerformed

